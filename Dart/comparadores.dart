void main() {
  
  //Comparadores
  //-----------------
  // > maior
  // >= maior ou igual
  // < menor
  // <= menor ou igual
  // == igual
  // != diferente
  
  bool testeComp = (10 > 20);
  print("COMP: $testeComp");

  
  // Operador OR ||
  // ------------------------
  // true true -> true
  // true false -> true
  // false true -> true
  // false false -> false
  
  
  bool testeOr = (false || true);
  print('OR: $testeOr ');
  
  
  // Operador AND &&
  //------------------------
  // true true -> true
  // true false -> false
  // false true -> false
  // false false -> false
  
  bool testeAnd = (false && true);
  print('AND: $testeAnd');
  
  
  
}

